        public List<City> SelectAll(City dtoObject)
        {
            List<City> obj = null;
            try
            {
                using (new Tracer((SQTLogType.DataAccessTraceLog)))
                {
                    //Log Method Parameteres.
                    ArrayList parameterList = new ArrayList();
                    if(dtoObject != null)
                        parameterList.Add(dtoObject);
                    SQTLogger.WriteLog(LogMessageType.MethodStart, parameterList, Common.GetMethodName, SQTLogType.DataAccessTraceLog);
                    if(dtoObject != null)  
                    {
                        obj = StoredProcedure(MasterConstant.CitySelectAll)
                                                .AddParameter("@CityID", dtoObject.CityID)
.AddParameter("@CityCode", dtoObject.CityCode)
.AddParameter("@CityName", dtoObject.CityName)
.AddParameter("@CountryID", dtoObject.CountryID)
.AddParameter("@StateID", dtoObject.StateID)
.AddParameter("@IsActive", dtoObject.IsActive)

                                                .WithTransaction(dbtr)
                                                .FetchAll<City>();
                    }
                    else
                    {
                        obj = StoredProcedure(MasterConstant.CitySelectAll)
                                                .WithTransaction(dbtr)
                                                .FetchAll<City>();
                    }
                }
            }
            catch (Exception ex)
            {
                //Log exception at DataAccess Layer.
                bool rethrow = ExceptionPolicy.HandleException(ex, SQTLogType.DataAccessLayerLog);
                if (rethrow)
                {
                    throw ex;
                }
            }
            return obj;
        }


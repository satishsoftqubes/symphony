        public List<ItemCategory> SelectAll(ItemCategory dtoObject)
        {
            List<ItemCategory> obj = null;
            try
            {
                using (new Tracer((SQTLogType.DataAccessTraceLog)))
                {
                    //Log Method Parameteres.
                    ArrayList parameterList = new ArrayList();
                    if(dtoObject != null)
                        parameterList.Add(dtoObject);
                    SQTLogger.WriteLog(LogMessageType.MethodStart, parameterList, Common.GetMethodName, SQTLogType.DataAccessTraceLog);
                    if(dtoObject != null)  
                    {
                        obj = StoredProcedure(MasterConstant.ItemCategorySelectAll)
                                                .AddParameter("@ItemCategoryID", dtoObject.ItemCategoryID)
.AddParameter("@ItemID", dtoObject.ItemID)
.AddParameter("@CategoryID", dtoObject.CategoryID)
.AddParameter("@OrderNo", dtoObject.OrderNo)

                                                .WithTransaction(dbtr)
                                                .FetchAll<ItemCategory>();
                    }
                    else
                    {
                        obj = StoredProcedure(MasterConstant.ItemCategorySelectAll)
                                                .WithTransaction(dbtr)
                                                .FetchAll<ItemCategory>();
                    }
                }
            }
            catch (Exception ex)
            {
                //Log exception at DataAccess Layer.
                bool rethrow = ExceptionPolicy.HandleException(ex, SQTLogType.DataAccessLayerLog);
                if (rethrow)
                {
                    throw ex;
                }
            }
            return obj;
        }


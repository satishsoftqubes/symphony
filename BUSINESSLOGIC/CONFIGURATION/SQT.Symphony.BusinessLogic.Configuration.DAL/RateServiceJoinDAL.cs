                    if (dtoObject != null)
                    {
                        obj = StoredProcedure(MasterConstant.RateServiceJoinSelectAll)
                                                .AddParameter("@RateServiceID", dtoObject.RateServiceID)
.AddParameter("@RateID", dtoObject.RateID)
